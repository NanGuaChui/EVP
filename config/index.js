const fs = require('fs');
const path = require('path');

const getWebpackAlias = chain => {
  // 手动添加src下的所有文件目录
  const srcPath = path.resolve(process.cwd(), 'src');
  let q = fs.readdirSync(srcPath);
  const alias = {};
  q.filter(e => {
    const dir = fs.statSync(path.join(srcPath, e));
    return dir.isDirectory();
  }).forEach(e => {
    alias[e] = path.join(srcPath, e);
  });
  chain.merge({ resolve: { alias: alias } });
};

